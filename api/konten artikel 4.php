         <div class="opening" style="padding-left: 0; padding-right: 0; margin-bottom: 0;" id="konten">
            <p style="text-indent: 0;">Oleh: Rizqi Aulia</p><br>
            <p>Dalam menjalani aktivitas sehari-hari, kita membutuhkan stamina dan kondisi kesehatan yang optimal. Kondisi kesehatan yang dimaksud bukan hanya kesehatan fisik namun juga kesehatan mental. Kesehatan mental adalah suatu keadaan dimana seseorang memahami dengan baik kemampuan dirinya, dapat mengatasi stres, bekerja dengan produktif, dan membuat kontribusi dalam komunitasnya (<span style="font-style: italic;">World Health Organization</span>). Kesehatan mental dapat mempengaruhi kondisi kesehatan fisik, begitu juga sebaliknya. Mental yang tidak sehat dapat menyebabkan gangguan suasana hati, kemampuan berpikir, serta kendali emosi yang mengarah pada perilaku negatif yang mungkin mengancam nyawa dan orang lain. </p>
            <p>Setiap orang memiliki resiko untuk terkena penyakit mental, tidak peduli berapapun usia, gender, suku, atau pendapatannya. Keadaan lingkungan baik sosial dan/atau finansial dapat membentuk kesehatan mental seseorang. Namun tentu saja seberapa baik kesehatan mental seseorang ditentukan oleh beragam faktor dan hal-hal yang terjadi dalam hidupnya. Menurut Kementrian Kesehatan Republik Indonesia, terdapat 3 jenis masalah kesehatan mental yang paling umum terjadi yaitu stres, gangguan kecemasan, dan depresi.</p>
            <p>Stres adalah keadaan ketika seseorang mengalami tekanan yang sangat berat, baik secara emosi maupun mental. Seseorang yang stres akan tampak gelisah, cemas, dan mudah tersinggung. Pada kasus tertentu, stres juga dapat memicu depresi. Beberapa dampak stres terhadap perilaku seseorang adalah,</p>
            <ul class="artlist" style="margin-bottom: 0;">
                <li>Menjadi penyendiri dan enggan berinteraksi dengan orang lain</li>
                <li>Kehilangan nasfsu makan atau makan berlebihan</li>
                <li>Marah yang sulit dikendalikan</li>
                <li>Menjadi perokok dan/atau merokok secara berlebihan</li>
                <li>Mengkonsumsi minuman beralkohol</li>
                <li>Penyalahgunaan narkotika</li>
            </ul>
            <p>Masalah kesehatan yang dapat timbul berupa,</p>
            <ul class="artlist" style="margin-bottom: 0;">
                <li>Sulit tidur (Insomnia) dan kelelahan</li>
                <li>Sakit kepala dan sakit perut</li>
                <li>Nyeri dada</li>
                <li>Nyeri otot</li>
                <li>Penurunan libido</li>
                <li>Obesitas</li>
                <li>Hipertensi</li>
                <li>Diabetes</li>
                <li>Gangguan jantung</li>
            </ul>
            <p>Banyak faktor yang menyebabkan seseorang mengalami stres, diantaranya adalah masalah keuangan, tuntutan pekerjaan, hubungan sosial, kekerasan dalam rumah tangga (KDRT), tugas yang tidak ada habisnya, sulit memahami pelajaran, dan sebagainya. Cara terbaik dalam menghadapi stress adalah menyelesaikan dan fokus pada masalah yang mengakibatkan stres. Misalnya ketika dihadapkan dengan tumpukan tugas yang dirasa tidak akan pernah selesai, maka kerjakanlah satu persatu, tidak perlu terburu-buru dan sebisa mungkin tidak <span style="font-style: italic;">deadliner</span> atau ketika mengalami KDRT maka mintalah bantuan pada saudara, tetangga, maupun aparat setempat.</p>
            <p>Gangguan Kecemasan Gangguan kecemasan adalah kondisi psikologis ketika seseorang mengalami rasa cemas berlebihan secara terus menerus dan tidak dapat dikendalikan, sehingga mengganggu aktivitas sehari-hari. Bagi sebagian orang normal, rasa cemas hanya timbul pada kejadian tertentu seperti ketika akan ujian, wawancara kerja, atau melamar doi. Namun pada penderita gangguan kecemasan, rasa cemas akan timbul di setiap situasi sehingga sulit untuk rileks yang mengarah pada stres. Gejala lain yang muncul adalah kurangnya rasa percaya diri, menjadi mudah marah, gugup, dan sulit berkonsentrasi. Selain itu, gejala fisik yang menyertai gangguan kecemasan antara lain,</p>
            <ul class="artlist" style="margin-bottom: 0;">
                <li>Insomnia</li>
                <li>Jantung berdebar</li>
                <li>Sesak napas</li>
                <li>Pusing</li>
                <li>Mulut kering</li>
                <li>Otot menjadi tegang</li>
                <li>Kesemutan</li>
                <li>Mengeluarkan keringat secara berlebihan</li>
            </ul>
            <p>Meskipun penyebab gangguan kecemasan belum diketahui secara pasti, terdapat faktor yang diduga memicu kondisi tersebut. Faktor tersebut berupa trauma akibat <span style="font-style: italic;">bullying</span>, pelecahan, atau KDRT.</p>
            <p>Faktor lainnya adalah ketidakseimbangan hormon serotonin dan <span style="font-style: italic;">noradrenalin</span> dalam otak yang berfungsi untuk mengendalikan suasana hati. Penyalahgunaan narkotika juga dapat memicu penyakit ini. Pengobatan mandiri bisa diupayakan dengan cara mengkonsumsi makanan bergizi, tidur yang cukup, mengurangi asupan kafein, berhenti merokok, berolahraga secara rutin, dan melakukan meditasi atau yoga jika perlu. Ketika pengobatan mandiri tidak memberikan perubahan, segera konsultasi dengan dokter. Dokter biasanya akan memberikan obat antiansietas dan terapi kognitif.</p>
            <p>Depresi merupakan gangguan suasana hati yang membuat penderitanya merasa sedih secara terus-menerus. Berbeda dengan kesedihan yang hanya berlangsung beberapa hari, depresi bisa berlangsung selama berminggu-minggu bahkan berbulan-bulan. Sama halnya dengan penyakit mental yang lain, depresi tidak hanya memengaruhi perasaan dan emosi, namun juga menyebabkan masalah fisik yang mengganggu aktivitas. Pada kasus tertentu, penderita dapat menyakiti diri sendiri hingga bunuh diri.</p>
            <p>Beberapa gejala seseorang mengalami depresi adalah,</p>
            <ul class="artlist" style="margin-bottom: 0;">
                <li>Kehilangan motivasi untuk melakukan sesuatu</li>
                <li>Merasa bersalah dan khawatir berlebihan</li>
                <li>Sulit membuat keputusan dan mudah tersinggung</li>
                <li>Terus merasa sedih dan menangis</li>
                <li>Kehilangan rasa percaya diri</li>
                <li>Tidak mempedulikan orang lain</li>
                <li>Memiliki pikiran untuk menyakiti diri sendiri atau bunuh diri</li>
            </ul>
            <p>Dampak depresi terhadap kesehatan fisik diantaranya,</p>
            <ul class="artlist" style="margin-bottom: 0;">
                <li>Insomnia dan kelelahan</li>
                <li>Kehilangan nafsu makan atau makan berlebihan</li>
                <li>Merasakan nyeri tanpa sebab</li>
                <li>Perubahan siklus menstruasi pada wanita</li>
                <li>Penurunan libido</li>
                <li>Respon lambat terhadap sesuatu</li>
            </ul>
            <p>Terdapat berbagai hal yang memicu depresi, mulai dari stres berlebihan, kehilangan orang yang dicintai, merasa kesepian, emosi yang tidak stabil, penderitaan akibat penyakit parah berkepanjangan, cedera di kepala, efek narkotika, dan faktor genetik dalam keluarga. Sangat dianjurkan untuk segera menemui dokter jika merasakan gejala depresi selama lebih dari dua minggu dan tidak kunjung mereda. Penanganan dokter akan disesuaikan dengan tingkat keparahan depresi yang diderita masing-masing pasien. Bentuk penanganan bisa berupa terapi konsultasi, pemberian obat antidepresi, atau keduanya.</p>
            <p>Jenis penyakit mental yang lain adalah gangguan panik, fobia (ketakutan akan sesuatu), <span style="font-style: italic;">Obsessive Compulsive Disorder</span> (OCD, merasa harus melakukan sesuatu berulang-ulang), <span style="font-style: italic;">Post-Traumatic Stress Disorder</span> (PTSD, depresi yang dipicu oleh trauma masa lalu), skizofrenia (halusinasi atau delusi), <span style="font-style: italic;">bipolar</span> (perubahan emosi secara drastis dan mendadak), paranoia (merasa terancam oleh sesuatu), dan masih banyak lagi.</p>
            <p>Penyakit mental adalah kondisi serius yang mempengaruhi pola pikir, emosi, dan tingkah laku yang bisa terjadi secara berkepanjangan. Hal ini dapat mempengaruhi kemampuan bersosialisasi, bekerja, dan belajar. Penyakit mental tidak bisa dianggap remeh. Jika Anda mengetahui saudara, kerabat, teman, atau bahkan Anda sendiri menunjukkan gejala penyakit mental, segera ditangani atau dibawa ke dokter. Penyakit mental bisa disembuhkan, meskipun terdapat kasus khusus dimana penyakit tersebut sulit disembuhkan.</p>
