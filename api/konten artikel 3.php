         <div class="opening" style="padding-left: 0; padding-right: 0; margin-bottom: 0;" id="konten">
            <p class="bold">Haloo teman jauh! Tahun akademik kali ini diawali dengan segala yang berbau istilah 'jarak jauh'. Mulai dari kuliah jarak jauh, rapat jarak jauh, ujian jarak jauh, seminar jarak jauh (webinar), adik kita yang sekolah jarak jauh, orang tua atau kerabat kita yang bekerja jarak jauh, dan masih banyak lagi. Keadaan tersebut mendorong kita untuk berkomunikasi dengan perangkat digital yang terhubung secara daring. Duduk di depan perangkat digital seperti komputer atau laptop ‘seharian’ tentunya punya dampak negatif, lho! Terus bagaimana ya biar kita tidak terkena dampak negatif tersebut? Yuk, simak artikel ini.</p>
            <p class="bold">Dampak Negatif? Apaan tuh? Berikut dampak negatif dari duduk terlalu lama depan perangkat digital yang telah sobat KSR rangkum:</p>
            <ol type="1" class="artol">
               <li>Pegal-pegal</li>
               <p>Kalau terlalu lama duduk depan layar dan berulang setiap harinya, pegal-pegal terasa dibagian punggung dan bahu. Terlebih jika kita menggunakan komputer dan laptop dalam posisi yang tidak benar. Otot dan tulang kita rasanya tegang. Siapa yang sebelum PJJ harus menyiapkan balsam dan koyo? xixixi.</p>
               <li>Mata cepat lelah</li>
               <p>Saat menatap layar, mata kita cenderung jarang untuk berkedip. Semakin lama terbuka, mata rasanya panas dan kekurangan cairan dari air mata saat kita berkedip. Apalagi jika ditambah dengan tingkat pencahayaan ruangan dan jarak mata dengan komputer yang terlalu dekat. Saking parahnya bisa sampai sakit kepala, penglihatan menjadi buram dan tidak beraturan. Istilah kerennya yaitu sindrom penglihatan komputer atau Computer Vision Syndrome (CVS).</p>
               <li><span style="font-style: italic;">Syndrome Tunnel Syndrome (CTS)</span></li>
               <p>Kalau sebelumnya istilah CVS, sekarang ada istilah lain lagi yaitu CTS. Wah baru dengar, istilah apa lagi tuh? CTS atau dalam Bahasa Indonesianya Sindrom Lorong Karpal, merupakan kondisi sensasi kesemutan, mati rasa, nyeri, lemah otot, bahkan pembengkakan pada bagian pergelangan tangan hingga lengan atas karena saraf terjepit di pergelangan tangan. Posisi tangan memegang mouse atau touchpad yang salah, posisi menulis yang tidak nyaman, dan sebagainya kerap menjadi penyebab CTS saat PJJ.</p>
               <li>Radiasi</li>
               <p>Ternyata layar monitor dari komputer, laptop, atau pun ponsel kita memancarkan radiasi, lho. Dampak radiasi ini bisa memengaruhi kelelahan mata dan sensasi pusing pening-pening. Kita sering tidak menyadari keberadaan radiasi ini.</p>
               <li>Stres dan Depresi</li>
               <p>Selain gangguan pada fisik, ada juga gangguan mental. Tekanan tugas-menugas, rapat silih berganti, belum lagi tuntutan akademik yang selalu diprioritaskan. Belum lagi, lingkungan di tempat tinggal yang terkadang kurang mendukung kita untuk melakukan PJJ. Dari pusing pening-pening, berlanjut ke stres, bahkan depresi. Duh, jangan sampai ya..</p>
            </ol>
            <p class="bold" style="text-indent: 0;">Terus Apa Tips-nya?</p>
            <p style="text-indent: 0;">Sobat KSR punya beberapa tips buat kalian pejuang PJJ dimana pun kalian berada. Berikut tipsnya.</p>
            <ol type="1" class="artol">
               <li>Udara dan cahaya dalam ruangan</li>
               <p>Pastikan saat menggunakan perangkat digital selama berjam-jam, kamu dalam ruangan dengan udara yang mengalir, tidak sempit, tidak pengap, tidak gelap, dan kecerahan layar monitor tidak menyakitkan mata.</p>
               <li>Posisi duduk yang benar</li>
               <p>Disarankan agar kamu menggunakan komputer atau laptop dalam keadaan duduk dengan meja dan kursi, tidak rebahan, dan tidak tiarap. Pastikan layar sejajar pandangan mata, punggung tegap, kaki menapak dengan nyaman, lengan bawah dalam posisi horizontal dengan meja, dan leher dalam keadaan rileks.</p>
               <li>Olahraga ringan</li>
               <p>Pagi hari sebelum PJJ dimulai, kamu boleh banget olahraga atau pemanasan dulu, seperti bersepeda, senam, jogging, dan lainnya agar lebih segar dan tidak gampang mengantuk. Lalu, selama perpidahan sesi, kamu dapat melakukan olahraga ringan seperti peregangan, senam ringan, atau hanya berjalan ke luar ruangan. Setidaknya, kamu bergerak dan memberikan waktu untuk mata beristirahat sejenak.</p>
               <li>Mengalihkan pandangan dari depan layar</li>
               <p>Sediakan waktu untuk mata beristirahat dari layar. Direkomendasikan untuk mengalihkan pandangan dari layar monitor dalam pola 20-20, setiap 20 menit dan selama 20 detik. Pastikan pula jarak layar monitor dengan mata dalam rentang aman yakni 50-100 cm.</p>
               <li>Beristirahat sejenak</li>
               <p>Bukan hanya mata yang perlu beristiharat, tubuh juga perlu. Saat tidak ada sesi, sempatkanlah tidur siang. Selain menyegarkan, tidur siang juga dapat meningkatkan konsentrasi.</p>
               <li>Mengenakan kacamata anti radiasi</li>
               <p>Guna mengurangi paparan dari radiasi, kamu bisa menggunakan kacamata anti radiasi. Bagi kamu yang memiliki gangguan penglihatan miopia atau silinder, jangan memaksakan matamu untuk melihat layar tanpa kacamata, karena dapat memperparah gangguan tersebut.</p>
               <li><span style="font-style: italic;">Sharing</span><span> dengan teman</span></li>
               <p>Kalian juga pastinya punya tips masing-masing. Boleh banget sharing dengan temanmu untuk dapat masukan satu sama lain.</p>
            </ol>
            <p>Wah, semoga tipsnya bisa kita terapkan ya. Tidak hanya saat perkuliahan saja, tetapi juga kapan pun saat kita sering bertatap muka dengan layar digital. PJJ makin nikmat, dampak negatif minggat. Ingat selalu pesan ibu, yaitu 3M (Memakai masker, Menjaga jarak, dan Mencuci Tangan). Semoga pandemi segera berakhir dan kita lekas bersua kembali di Otista 64C.</p>
