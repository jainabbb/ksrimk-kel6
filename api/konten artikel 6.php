            <p style="font-weight: bold; text-indent: 0">Kisah pilu dari Palu</p>
            <p>Kinta, sebuah kampung tua di Palu, Sulawesi Tenggara ternyata bertuah. Di kampung itu berdiri 15 rumah saling berhadapan. Sesaat setelah gempa besar yang disusul tsunami tepat setahun silam, ratusan rumah di Perumnas Balaroa yang tidak jauh dari Kampung Kinta hancur dilumat lumpur beserta harta benda dan penghuninya.  Sebagian warga kampung yang berlarian ke luar kampung juga ikut ditelan lumpur. Nasib baik bagi mereka yang tetap bertahan di kampung, mereka selamat.</p>
            <p>Dalam bahasa Suku Kaili, etnik mayoritas di Palu, Kinta berarti <span style="font-style: italic;">kampung kecil</span>. Sedangkan dalam Kamus Kaili Ledo-Indonesia-Inggris, kinta berarti <span style="font-style: italic;">tanah untuk rumah</span>.</p>
            <p>Konon, para tetua tidak berani membuat rumah di luar Kinta karena dulunya wilayah di sekitar Kinta adalah rawa. Memang wilayah Perumnas Balaroa dulunya bernama Lonjo yang artinya <span style="font-style: italic;">tanah berlumpur</span>. Dalam kamus yang sama, terdapat lema <span style="font-style: italic;">nelonjo</span> yang artinya <span style="font-style: italic;">tenggelam dalam lumpur</span>. Lonjo disulap menjadi Balaroa dan dibangun pemukiman di atasnya karena mudahnya akses air. Warga cukup membuat sumur berkedalaman empat meter untuk mendapat air bersih. Sedangkan di Kinta, sumur warga rata-rata berkedalaman sembilan meter.</p>
            <p>Namun, pengurukan rawa Lonjo tersebut berbuah pahit. Bekas rawa mengandung air jenuh yang bila terjadi pergerakan sesar Bumi yang cukup kuat, air tersebut dapat mencuat dan mengubah tanah di atasnya menjadi lumpur basah.</p>
            <p>Bukan hanya dalam penamaan daerah, kearifan tetua yang dapat dijadikan pedoman sederhana mitigasi bencana juga terekama dalam bentuk syair yang berjudul Kayori.</p>
            <br>
            <p style="text-indent: 0"><span style="font-style: italic;">Goya-goya Gontiro</span> (Goyang-goyang di Desa Ganti)</p>
            <p style="text-indent: 0"><span style="font-style: italic;">Toka Bonga Lolio</span> (Yang melihat ke bawah, orang Desa Kabonga dan Loli Oge)</p>
            <p style="text-indent: 0"><span style="font-style: italic;">Palu, Tondo, Mamboro, matoyomo</span> (Palu, Tondo, dan Mamboro tenggelam)</p>
            <p style="text-indent: 0"><span style="font-style: italic;">Kamolue melantomo</span> (Tinggal Kayumalue terapung)</p>
            <br>
            <p>Syair berjudul Kayori itu menceritakan peristiwa gempa dan tsunami di Teluk Palu pada 1938. Kayori berjejak dalam ingatan sebagian warga Kayumalue, Kecamatan Palu Utara.</p>
            <br>
            <p style="font-weight: bold; text-indent: 0">Syair Tuah dari Aceh</p>
            <p>Gempa dan tsunami mematikan yang terjadi pada Minggu pagi tanggal 24 Desember 2004 menewaskan dan menghilangkan ratusan ribu jiwa di Indonesia, Malaysia, Thailand, Bangladesh, hingga pantai Timur Afrika. Namun, korban jiwa yang ada di Pulau Simelue, Aceh, hanya berjumlah enam orang. Hal ini karena warga Simelue mempunyai tutur syair yang diturunkan oleh tetua tentang mitigasi bencana.</p>
