<div class="opening" style="padding-left: 0; padding-right: 0; margin-bottom: 0;" id="konten">
    <p>Menjelang hari raya dan libur panjang, mudik sudah menjadi tradisi bagi sebagian orang. Moda tranportasi yang digunakan untuk mudik beragam, mulai dari bus, kereta api, pesawat terbang, dan kapal. Biasanya, perjalanan mudik memakan waktu berjam-jam. Oleh karenanya, kesehatan tubuh selama mudik harus dijaga agar perjalanan lancar dan sampai kampung halaman dengan selamat. Berikut tips agar mudik tetap tubuh tetap sehat.</p>
    <br>
    <strong>Istirahat Cukup</strong>
    <p>Kelelahan merupakan keluhan utama dalam perjalanan panjang dan lama. Kelelahan dapat menurunkan daya tahan tubuh sehingga tubuh dapat dengan mudah terinfeksi kuman, virus,dan bakteri penyebab penyakit. Istirahat yang cukup dapat mengurangi keluhan ini. Agar tubuh tetap bugar di perjalanan, usahakan tubuh sudah tidur nyenyak minimal 6 jam.</p>
    <br>
    <strong>Makanan Sehat</strong>
    <p>Mudik dengan perut kosong tidak baik untuk kesehatan. Sebelum berangkat, usahakan tubuh sudah mengonsumsi makanan bergizi seimbang dan air mineral yang cukup. Hindari memakan makanan yang dapat memicu kembung, seperti buncis, kubis, sawi putih, brokoli, bawang, dan telur. Perbanyak makanan yang mengandung protein dan vitamin.</p>
    <p>Selama perjalanan, dianjurkan untuk membawa bekal dari indekos. Selain menghemat pengeluaran, bekal yang dibawa juga lebih terjaga kebersihannya. Bekal yang dianjurkan adalah makanan kering yang siap santap. Bila membawa makanan basah, usahakan tidak dikemas dalam keadaan panas dan dikonsumsi maksimal sebelum 6-8 jam waktu masak. Air mineral yang cukup dan buah-buahan juga penting dibawa sebagai bekal. Hindari bekal yang mengandung soda, asam berlebih, atau terlalu pedas karena dapat mengganggu pencernaan selama perjalanan.</p>
    <p>Bila terpaksa untuk membeli makan di perjalanan, hindari makanan yang kurang laik, seperti terpapar sinar matahari langsung, tidak terjaga kebersihannya, dan makanan yang cepat saji.</p>
    <br>
    <strong>Sedia Obat</strong>
    <p>Obat-obatan merupakan hal yang penting untuk dibawa ketika mudik. Obat ringan, seperti obat herbal terstandar, dapat dibawa sebagai bentuk kehati-hatian. Bila tubuh sedang dalam kondisi tidak fit, terlebih dahulu konsultasikan kepada dokter mengenai obat yang harus dikonsumsi dan anjuran mudik atau tidaknya.</p>
    <br>
    <strong>Perjalanan Nyaman</strong>
    <p>Dalam perjalanan, usahakan menghindari pakaian ketat yang dapat menghalangi kulit untuk bernapas, seperti celana jin yang ketat. Pakaian yang ketat dapat membuat penguapan keringat terhalang sehingga kulit dapat dengan mudah terinfeksi jamur.</p>
    <p>Perjalanan panjang selama mudik juga dapat membuat tubuh mudah lelah. Atur posisi tempat duduk sedikit rebah ke belakang agar tubuh tidak terlalu membungkuk selama perjalanan.</p>
    <p>Dianjurkan untuk melakukan perjalanan pada malam hari. Ketika malam, suhu lingkungan lebih bersahabat dibanding siang. Perjalanan di malam hari juga dapat memungkinkan tubuh untuk menerima asupan makanan, khususnya bagi yang berpuasa.</p>
    <br>
    <strong>Olahraga Ringan</strong>
    <p>Bila otot mulai terasa kaku dan pegal, lakukan olahraga ringan. Olahraga ringan dapat dilakukan dengan cara berikut ini.</p>
    <ul class="artlist">
        <li>Gerakkan otot betis dan kaki secara teratur, kencangkan dan kendurkan, otot-otot bokong dan perut untuk mendorong darah kembali ke jantung.</li>
        <li>Angkatlah kedua tumit, bola kaki tetap berada di lantai, kemudian kembali ke posisi semula.</li>
        <li>Kedua tumit tetap tinggal di lantai, angkat kedua bola kaki, kembalikan ke posisi awal.</li>
        <li>Luruskan kaki sejauh ruangan yang ada dan memungkinkan.</li>
        <li>Kencangkan otot-otot paha bagian depan, tahan selama 2-3 detik, lalu kembali ke posisi awal.</li>
        <li>Tekankan kedua otot betis Anda ke tempat duduk bagian depan, kencangkan otot-otot di bagian belakang dari paha, tahan 2-3 detik, kemudian kembali ke posisi awal.</li>
        <li>Usahakan pinggang Anda ditekankan dengan kuat pada tempat duduk, tariklah otot-otot perut ke dalam, tahan selama 2-3 detik, kemudian kembali ke posisi awal.</li>
        <li>Tekankan otot-otot bokong sekuat yang Anda mampu pada tempat duduk selama 2-3 detik, kemudian kembali ke posisi awal. Geser berat badan dari kanan ke kiri dari pantat Anda, ayunkan ke belakang dan ke depan selama 5-10 detik.</li>
        <li>Gerakkan pinggang ke depan sehingga terjadi lengkungan pada pinggang.</li>
        <li>Tekankan pinggang ke tempat duduk dan pinggul didorong ke depan.</li>
    </ul>
    <br>
    <strong>Bagasi Penuh</strong>
    <p>Tidak dianjurkan untuk membawa bagasi terlalu berat. Selain merepotkan, bagasi yang terlalu berat juga dapat membuat otot cepat lelah. Bagasi dapat dikirim terlebih dahulu lewat jasa pengiriman barang.</p>
