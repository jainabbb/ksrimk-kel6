            <h2>B. Tujuan</h2>
            <p>Tujuan dibentuknya KSR di masing-masing kampus tidak jauh berbeda. Beberapa tujuan dibentuknya UKM Palang Merah (UPM) STIS diantaranya:</p>
            <ul>
               <li>meningkatkan kemampuan berorganisasi</li>
               <li>menyiapkan sumber daya yang memiliki kemampuan pertolongan pertama di lingkungan mahasiswa STIS</li>
               <li>meningkatkan kemampuan ke-PMI-an dan Keintelektualan</li>
               <li>mengembangkan rasa kemanusiaan dan sosial kepada mahasiswa</li>
               <li>sosialisasi peran dan fungsi KSR terhadap masyarakat.</li>
            </ul>
